#include <unistd.h>
#include "libft.h"

void		ft_putnbr(int n)
{
	char	a;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = ABS(n);
	}
	if (n >= 10)
	{
		a = (n % 10) + '0';
		ft_putnbr(n / 10);
		write(1, &a, 1);
	}
	else
	{
		a = n + '0';
		write(1, &a, 1);
	}
}
